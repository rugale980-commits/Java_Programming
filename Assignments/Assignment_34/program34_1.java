import java.util.*;

public class Pattern
{   
    public void DisplayASCII()
    {

        int iCnt = 0;
        char ch = '\0';
        char chDisply = '\0';

        System.out.println("-----------------------------------------------------------\n");
        System.out.println(" Decimal\tHex\tOctal\tCharacter\n");
        System.out.println("-----------------------------------------------------------\n");

        for (iCnt = 0; iCnt <= 255; iCnt++)
        {
            ch = (char)iCnt;

            if ((iCnt >= 32) && (iCnt <= 126))
            {
                chDisply = ch;
            }
            else
            {
                chDisply = '.';
            }
            System.out.printf(" %3d\t\t%02X\t%03o\t   %c\n", iCnt, iCnt, iCnt, chDisply);
        }
        System.out.println("-----------------------------------------------------------\n");     
    }    

} // End of Method

////////////////////////////////////////////////////////////////////////////////
//
//  Main Entry point application
//
////////////////////////////////////////////////////////////////////////////////

public class program34_1
{
    public static void main(String A[])
    {
        Pattern pobj = new Pattern();

        pobj.DisplayASCII();
    }

} // End of main

